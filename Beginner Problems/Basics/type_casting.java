public class type_casting {

  public static void main(String[] args) {
    //type casting
    //here x is implicitly converted into y
    double x = 10.7;
    //explicitly
    int y = (int)x;
    System.out.println(y);
    //constant value
    final int num = 10;
    System.out.println(num);

    

  }
  
}
