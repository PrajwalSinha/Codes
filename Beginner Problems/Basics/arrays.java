public class arrays {
  
  public static void main(String[] args) {
    //arrays
    int[] arr = new int[5];
    arr[0] = 1;
    arr[1] = 6;
    arr[2] = 7;
    arr[3] = 9;
    arr[4] = 1;
    System.out.println(arr[2]);

    //2d array
    int[][] ar = {{6,5},{7,8,9},{7,4,3},{8}};
    System.out.println(ar[1][2]);
  }
}
