#include <bits/stdc++.h>
using namespace std;

int main(){
  //range -> [-10^9,10^9]
  int numInt = INT_MAX;
  cout << numInt<<endl;

  //long -> no till 10^12
  long num = 10000000000;
  cout<<num;

  //10^18
  long long numLong = 100000000000000;
  cout << numLong;

  //till 7 decimal places
  float numFloat = 8.7;
  cout << numFloat;

  //till 15 decimal places -> double

  char ch = 'a';
  cout << ch <<endl;

  string str = "tuf";
  cout << str;

  //I/O
  int bdate;
  int bd;
  cin >> bdate;

  cout << bdate << endl;
  cout << bd; //assign a garbage value
  return 0;
}